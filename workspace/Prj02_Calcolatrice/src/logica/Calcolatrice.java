package logica;

public class Calcolatrice {

	public double addizione(double addendo1, double addendo2) {
		
		double risultato = addendo1 + addendo2;
		
		return risultato;
		
	}
	public double sottrazione(double addendo1, double addendo2) {
		
		double risultato = addendo1 - addendo2;
		
		return risultato;
		
	}
	public double moltiplicazione(double addendo1, double addendo2) {
		
		double risultato = addendo1 * addendo2;
		
		return risultato;
		
	}
	public double divisione(double addendo1, double addendo2) {
		
		double risultato = addendo1 / addendo2;
		
		return risultato;
		
	}
	
	
	
}
